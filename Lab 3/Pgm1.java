public class Pgm1 {
    
    public static void main(String[] args) {
        // Loop through numbers 1 to 15
        for (int i = 1; i <= 15; i++) {
            // Calculate square of the number
            int square = i * i;
            // Print the square
            System.out.println("Square of " + i + " is: " + square);
        }
    }
}
